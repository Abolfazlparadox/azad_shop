document.addEventListener('DOMContentLoaded', function () {
    const colorInput = document.getElementById('id_color');
    const valueInput = document.getElementById('id_value');

    if (colorInput && valueInput) {
        // بررسی تغییرات در ورودی رنگ
        colorInput.addEventListener('input', function () {
            const hexColor = colorInput.value;

            // بررسی رنگ در دیکشنری
            if (colorNames[hexColor]) {
                // اگر رنگ در دیکشنری موجود بود، نام فارسی رنگ را به input بگذار
                valueInput.value = colorNames[hexColor];
            } else {
                // اگر رنگ موجود نبود (در دیکشنری نباشد)، یک پیام پیش‌فرض یا خالی بگذار
                valueInput.value = "نامشخص";
            }
        });
    }
});